class ProverImpl final : public Prover::Service
{
private:
    // Result of the setup
    libzeth::CircuitWrapper<
        FieldT,
        HashT,
        HashTreeT,
        ppT,
        ZETH_NUM_JS_INPUTS,
        ZETH_NUM_JS_OUTPUTS>
        prover;
    keyPairT<ppT> keypair;

public:
    explicit ProverImpl(
        libzeth::CircuitWrapper<
            FieldT,
            HashT,
            HashTreeT,
            ppT,
            ZETH_NUM_JS_INPUTS,
            ZETH_NUM_JS_OUTPUTS> &prover,
        keyPairT<ppT> &keypair)
        : prover(prover), keypair(keypair)
    {
    }

    Status GetVerificationKey(
        ServerContext *, const Empty *, VerificationKey *response) override
    {
        std::cout << "[ACK] Received the request to get the verification key"
                  << std::endl;
        std::cout << "[DEBUG] Preparing verification key for response..."
                  << std::endl;
        try {
            PrepareVerifyingKeyResponse<ppT>(this->keypair.vk, response);
        } catch (const std::exception &e) {
            std::cout << "[ERROR] " << e.what() << std::endl;
            return ::grpc::Status(
                ::grpc::StatusCode::INVALID_ARGUMENT, grpc::string(e.what()));
        } catch (...) {
            std::cout << "[ERROR] In catch all" << std::endl;
            return ::grpc::Status(::grpc::StatusCode::UNKNOWN, "");
        }

        return Status::OK;
    }

    Status Prove(
        ServerContext *,
        const ProofInputs *proofInputs,
        ExtendedProof *proof) override
    {
        std::cout << "[ACK] Received the request to generate a proof"
                  << std::endl;
        std::cout << "[DEBUG] Parse received message to compute proof..."
                  << std::endl;

        // Parse received message to feed to the prover
        try {
            FieldT root = libzeth::string_to_field<FieldT>(proofInputs->root());
            libzeth::bits64 vpub_in =
                libzeth::hexadecimal_value_to_bits64(proofInputs->inpubvalue());
            libzeth::bits64 vpub_out = libzeth::hexadecimal_value_to_bits64(
                proofInputs->outpubvalue());
            libzeth::bits256 h_sig_in =
                libzeth::hexadecimal_digest_to_bits256(proofInputs->hsig());
            libzeth::bits256 phi_in =
                libzeth::hexadecimal_digest_to_bits256(proofInputs->phi());

            if (ZETH_NUM_JS_INPUTS != proofInputs->jsinputs_size()) {
                throw std::invalid_argument("Invalid number of JS inputs");
            }
            if (ZETH_NUM_JS_OUTPUTS != proofInputs->jsoutputs_size()) {
                throw std::invalid_argument("Invalid number of JS outputs");
            }

            std::cout << "[DEBUG] Process every inputs of the JoinSplit"
                      << std::endl;
            std::array<libzeth::JSInput<FieldT>, ZETH_NUM_JS_INPUTS> jsInputs;
            for (int i = 0; i < ZETH_NUM_JS_INPUTS; i++) {
                proverpkg::JSInput receivedInput = proofInputs->jsinputs(i);
                libzeth::JSInput<FieldT> parsedInput =
                    ParseJSInput<FieldT>(receivedInput);
                jsInputs[i] = parsedInput;
            }

            std::cout << "[DEBUG] Process every outputs of the JoinSplit"
                      << std::endl;
            std::array<libzeth::ZethNote, ZETH_NUM_JS_OUTPUTS> jsOutputs;
            for (int i = 0; i < ZETH_NUM_JS_OUTPUTS; i++) {
                proverpkg::ZethNote receivedOutput = proofInputs->jsoutputs(i);
                libzeth::ZethNote parsedOutput = ParseZethNote(receivedOutput);
                jsOutputs[i] = parsedOutput;
            }

            std::cout << "[DEBUG] Data parsed successfully" << std::endl;
            std::cout << "[DEBUG] Generating the proof..." << std::endl;
            extended_proof<ppT> ext_proof = this->prover.prove(
                root,
                jsInputs,
                jsOutputs,
                vpub_in,
                vpub_out,
                h_sig_in,
                phi_in,
                this->keypair.pk);

            std::cout << "[DEBUG] Displaying the extended proof" << std::endl;
            ext_proof.dump_proof();
            ext_proof.dump_primary_inputs();

            std::cout << "[DEBUG] Preparing response..." << std::endl;
            PrepareProofResponse<ppT>(ext_proof, proof);

        } catch (const std::exception &e) {
            std::cout << "[ERROR] " << e.what() << std::endl;
            return ::grpc::Status(
                ::grpc::StatusCode::INVALID_ARGUMENT, grpc::string(e.what()));
        } catch (...) {
            std::cout << "[ERROR] In catch all" << std::endl;
            return ::grpc::Status(::grpc::StatusCode::UNKNOWN, "");
        }

        return Status::OK;
    }
};

std::string Version()
{
    char buffer[100];
    int n;
    // Defined in the zethConfig file
    n = snprintf(
        buffer, 100, "Version %d.%d", ZETH_VERSION_MAJOR, ZETH_VERSION_MINOR);
    if (n < 0) {
        return "Version <Not specified>";
    }
    std::string version(buffer);
    return version;
}

